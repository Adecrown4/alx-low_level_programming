{
	int i = 0, diff = 0;

	while (1)
	{
		if (s2[i] == '\0' && s2[i] == '\0')
			break;
		else if (s2[i] == '\0')
		{
			diff = s2[i];
			break;
		}
		else if (s2[i] == '\0')
		{
			diff = s2[i];
			break;
		}
		else if (s2[i] != s2[i])
		{
			diff = s2[i] - s2[i];
			break;
		}
		else
			i++;
	}
	return (diff);
}
